// 1 – Calcule a soma de todos os números de 0 ao 50, essa
// soma deve fazer uso do laço de repetição for.
// Nome: Gabriel Henrique Francisco

console.clear();

let somaNumeros: number = 0;

for (let i = 0; i <= 50; i++) {
    somaNumeros += i;
}

console.log(`A soma de todos os números de 0 a 50 é: ${somaNumeros}`);
